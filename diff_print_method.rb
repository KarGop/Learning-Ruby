#Print method: No line break
print "Hello"  #Hello
print "World" #HelloWorld
print "\n"   #cursor will move to next line

#puts method: Will add line break at the end
puts "Hello"  #Hello
puts "World"   #World
puts  #new line

#p method: output with Double_quotes
p "Hello world!" #"Hello world!"
p "Hi there, this is a
big line break"  #"Hi there, this is a\nbig line break"
puts "Hi there, this is a
big line break"  #Hi there, this is a
                  #big line break
